namespace WeChat\Tools;


class Verify
{

    // +----------------------------------------------------------------------
    // | 定义
    // +----------------------------------------------------------------------

    // +----------------------------------------------------------------------
    // | 绑定
    // +----------------------------------------------------------------------

    // +----------------------------------------------------------------------
    // | 获取器
    // +----------------------------------------------------------------------

    // +----------------------------------------------------------------------
    // | 方法
    // +----------------------------------------------------------------------

    /**
     * 验证签名
     *
     * @param $arr
     * @param $signature
     *
     * @return bool
     */
    static public function signature ( $arr , $signature )
    {
        sort( $arr , SORT_STRING );
        $tmpStr = join( '' , $arr );
        $tmpStr = sha1( $tmpStr );

        return $signature === $tmpStr;
    }
}